/**
 * NCM Vote Type Enum
 * Defines the type of vote on a No Confidence Motion
 */
export enum NcmVoteType {
  /**
   * FOR - Vote in favor of removing the sabhapati
   */
  FOR = 'FOR',

  /**
   * AGAINST - Vote against removing the sabhapati
   */
  AGAINST = 'AGAINST',

  /**
   * ABSTAIN - Abstain from voting
   */
  ABSTAIN = 'ABSTAIN',
}

